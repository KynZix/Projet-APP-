      			<p>Basé à Londres, Orlebar Brown a été fondé en mars 2007 avec pour mission de proposer une approche plus sophistiquée des shorts de bain pour homme. Plus qu’un short de bain, “un short avec lequel on peut se baigner”, nos shorts sont des pièces essentielles pour des vacances élégantes – quelque chose que vous pouvez porter à la plage ou au bord de la piscine, et qui est suffisamment habillé pour que vous le portiez pour boire une verre ou déjeuner.</p>


